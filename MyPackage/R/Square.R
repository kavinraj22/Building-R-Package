#' Square of a number
#'
#' Takes in any numeric value and squares it
#' @param A numeric value to be squared
#' @return The square of the input
#' @export

square <- function(x) {
  return(x^2)
}

#' Cube of a number
#'
#' Takes in any numeric value and cube it
#' @param A numeric value to be squared
#' @return The cube of the input
#' @export
#'
cube <- function(y) {
  return(y^3)
}
